void TIM6_IRQHandler(void)   //TIM6中断
{
	if (TIM_GetITStatus(TIM6, TIM_IT_Update) != RESET) //检查指定的TIM中断发生与否:TIM 中断源 
		{
		TIM_ClearITPendingBit(TIM6, TIM_IT_Update  );  //清除TIMx的中断待处理位:TIM 中断源 
		    KEY_Scan();//按键扫描
			//蜂鸣器响一段时间
			if(beep_count > 0) {				
				beep_count --;
			}
			if(beep_state == 1 && beep_count == 0) {
				BEEP = 0;
				beep_state = 0;
			}
			//开关机画面持续一段时间
			if(power_count >0) {
				power_count --;
			}
			if(power_state == 1 && power_count ==5) {
				power_off();
			}
			if(power_state == 0 && power_count ==5) {
	            MODE_SWITCH();
			}
		}
}











